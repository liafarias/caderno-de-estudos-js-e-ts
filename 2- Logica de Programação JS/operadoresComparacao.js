/**
 * > Maior que
 * >= Maior que ou igual a
 * < Menor que
 * <= Menor que ou igual a
 * == Igualdade *****
 * === Igualdade estrita (valor e tipo igual)
 * != Diferença *****
 * !== Diferença estrita (valor e tipo)
 * SEMPRE RETORNA UM BOLEANO (TRUE OU FALSE)
 */

// EXEMPLOS
console.log(10 > 5); // true
console.log(10 >= 10); // true
console.log(6 < 8); // true
console.log(8 <= 6); // false
console.log(10 === 10); // true
console.log(5 !== 5); // false
console.log(10 === '10'); // false

