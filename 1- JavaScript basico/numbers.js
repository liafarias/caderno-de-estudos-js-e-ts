let num1 = 1;    //number
let num2 = 2.5; //number com ponto flutuante

console.log(num1 + num2);

// EXPRESSÕES MAIS USADAS

console.log(num1.toString() + num2); // a expressão "toString" serve para transformar o numero em uma string

console.log(num1.toFixed(2)); // Arrendodar para numero de casas (x = numero de casas)

console.log(Number.isInteger(num1)); // Para verificar se o numero é inteiro (retorna true ou false)

console.log(Number.isNaN(num1)); // Para verificar se o numero NÃO é valido(NaN) (retorna true ou false)