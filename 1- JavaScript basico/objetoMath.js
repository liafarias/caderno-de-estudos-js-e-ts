// METODO UTILIZADO PARA ARREDONDAR OS NUMEROS PARA CIMA OU PARA BAIXO E MUITO MAIS.


let num1 = 9.54578;


let num2 = Math.floor(num1); // Arredonda numero inteiro mais perto para baixo.

let num3 = Math.ceil(num1); // Arredonda numero interiro mais perto para cima.

let num4 = Math.round(num1); // Arredonda para o numero inteiro mais proximo (De 5 para cima arredonda para cima e abaixo de 5 ele arredonda para baixo).

let num5 = Math.random(); // Retorna um numero aleatorio diferente sempre que for chamado.

console.log(num2);
console.log(num3);
console.log(num4);
