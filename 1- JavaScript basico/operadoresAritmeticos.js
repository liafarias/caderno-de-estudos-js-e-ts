console.log(num1 * num2); // MULTIPLICAÇÃO
console.log(num1 / num2); // DIVISÃO
console.log(num1 ** num2); // POTENCIAÇÃO
console.log(num1%num2); // RESTO DA DIVISÃO (sempre que uma divisão for inteiro vai retornar zero, quando não for vai retornar 1)

